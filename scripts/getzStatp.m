model = fitlme(tbl(tbl.Stable==1,:),'VS~1+(1|monkey)');
null = fitlme(tbl(tbl.Stable==1,:),'VS~-1+(1|monkey)');
comp = compare(null,model);
p(2,3) = double(comp.pValue(2));
chi2(2,3) = double(comp.LRStat(2));
model = fitlme(tbl(tbl.Equi==1,:),'VS~1+(1|monkey)');
null = fitlme(tbl(tbl.Equi==1,:),'VS~-1+(1|monkey)');
comp = compare(null,model);
p(2,4) = double(comp.pValue(2));
chi2(2,4) = double(comp.LRStat(2));


model = fitlme(tbl,'RRE~1+(1|monkey)');
