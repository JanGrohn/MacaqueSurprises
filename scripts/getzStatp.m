function [chi2 p] = getzStatp(region,DT)
p = nan(3,4);
chi2 = nan(3,4);

tbl = table;
[VS idx] = unique(DT.(region)(:,2),'stable');
idx = idx(~isnan(VS));
VS = VS(~isnan(VS));
tbl.VS = VS;
tbl.RRE = unique(DT.(region)(~isnan(DT.(region)(:,1)),3),'stable');
tbl.SRPE = unique(DT.(region)(~isnan(DT.(region)(:,1)),1),'stable');
tbl.monkey = DT.monkey(idx);
tbl.Changing = DT.Changing(idx);
tbl.Stable = DT.Stable(idx);
tbl.Equi = DT.Equiprobable(idx);
model = fitlme(tbl,'SRPE~1+(1|monkey)');
null = fitlme(tbl,'SRPE~-1+(1|monkey)');
comp = compare(null,model);
p(1,1) = double(comp.pValue(2));
chi2(1,1) = double(comp.LRStat(2));
model = fitlme(tbl(tbl.Changing==1,:),'SRPE~1+(1|monkey)');
null = fitlme(tbl(tbl.Changing==1,:),'SRPE~-1+(1|monkey)');
comp = compare(null,model);
p(1,2) = double(comp.pValue(2));
chi2(1,2) = double(comp.LRStat(2));
model = fitlme(tbl(tbl.Stable==1,:),'SRPE~1+(1|monkey)');
null = fitlme(tbl(tbl.Stable==1,:),'SRPE~-1+(1|monkey)');
comp = compare(null,model);
p(1,3) = double(comp.pValue(2));
chi2(1,3) = double(comp.LRStat(2));
model = fitlme(tbl(tbl.Equi==1,:),'SRPE~1+(1|monkey)');
null = fitlme(tbl(tbl.Equi==1,:),'SRPE~-1+(1|monkey)');
comp = compare(null,model);
p(1,4) = double(comp.pValue(2));
chi2(1,4) = double(comp.LRStat(2));

model = fitlme(tbl,'VS~1+(1|monkey)');
